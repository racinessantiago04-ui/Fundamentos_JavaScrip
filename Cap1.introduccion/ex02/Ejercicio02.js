/*
    Uso del la palabra reservada 'let' para declarar una variable llamada 'bebida' y asignarle diferentes valores a lo largo del código. Cada vez que se asigna un nuevo valor a la variable 'bebida', se muestra su contenido en la consola.

    Diferencia entre var y let: La palabra reservada 'var' tiene un alcance de función, lo que significa que la variable declarada con 'var' es accesible dentro de la función en la que se declara. Por otro lado, 'let' tiene un alcance de bloque, lo que significa que la variable declarada con 'let' solo es accesible dentro del bloque en el que se declara (por ejemplo, dentro de un bucle o una estructura condicional). Además, 'let' no permite redeclarar la misma variable dentro del mismo bloque, mientras que 'var' sí lo permite.
*/

let bebida; // Declaración de la variable "bebida"
bebida = "Coca-Cola"; // Asignación del valor "Coca-Cola" a la variable "bebida"
console.log(bebida); // Imprime el valor de la variable "bebida" en la consola del navegador

bebida = "Pepsi"; // Reasignación de la variable "bebida" con un nuevo valor "Pepsi"
console.log(bebida); // Imprime el nuevo valor de la variable "bebida" en la consola del navegador

bebida = "Fanta"; // Reasignación de la variable "bebida" con un nuevo valor "Fanta"
console.log(bebida); // Imprime el nuevo valor de la variable "bebida" en la consola del navegador

// uso de const para declarar una variable constante llamada "PI" y asignarle el valor de 3.1416. Las variables declaradas con const no pueden ser reasignadas, lo que significa que su valor no puede cambiar a lo largo del código.

const PI = 3.1416; // Declaración de una variable constante llamada "PI" y asignación del valor 3.1416
console.log(PI); // Imprime el valor de la variable constante "PI" en la consola del navegador